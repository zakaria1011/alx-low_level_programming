#include "3-calc.h"
/**
 * op_add - addition
 * @a: first num
 * @a:seconde num
 * Return : result
 */
/**
 * op_sub - subtraction
 * @a: first num
 * @a:seconde num
 * Return : result
 */
/**
 * op_mul - multiplication
 * @a: first num
 * @a:seconde num
 * Return : result
 */
/**
 * op_div - divition
 * @a: first num
 * @a:seconde num
 * Return : result
 */
/**
 * op_mod - modulo
 * @a: first num
 * @a:seconde num
 * Return : result
 */
int op_add(int a, int b)
{
return (a + b);
}
int op_sub(int a, int b)
{
return (a - b);
}
int op_mul(int a, int b)
{
return (a * b);
}
int op_div(int a, int b)
{
return (a / b);
}
int op_mod(int a, int b)
{
return (a % b);
}
