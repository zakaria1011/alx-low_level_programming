#include "3-calc.h"
/**
 * op_add - addition
 * @a: first int
 * @b: seconde int
 * Return: result
 */
 /**
 * op_sub - addition
 * @a: first int
 * @b: seconde int
 * Return: result
 */
/**
 * op_mul - addition
 * @a: first int
 * @b: seconde int
 * Return: result
 */
/**
 * op_div - addition
 * @a: first int
 * @b: seconde int
 * Return: result
 */
/**
 * op_mod - addition
 * @a: first int
 * @b: seconde int
 * Return: result
 */
int op_add(int a, int b)
{
return (a + b);
}
int op_sub(int a, int b)
{
return (a - b);
}
int op_mul(int a, int b)
{
return (a * b);
}
int op_div(int a, int b)
{
return (a / b);
}
int op_mod(int a, int b)
{
return (a % b);
}
